package JavaLang.basics;

public class LogicalDemo {

    public static void main(String[] args) {


        /**
         * logical operators -> boolean output
         * && -  and -> two operands
         * || - or  -> two operands
         * ! - not -> one operand
         */

        boolean x = true;
        boolean y = false;

        System.out.println(x && y);
        System.out.println(x || y);


    }
}
