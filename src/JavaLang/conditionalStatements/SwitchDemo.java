package JavaLang.conditionalStatements;

public class SwitchDemo {
    public static void main(String[] args) {

        String day = "harsha";

        switch (day){
            case "mon":
                System.out.println("ill go to clg!!");
                break;
            case "sun":
                System.out.println("ill go to movie!");
                break;
            case "sat":
                System.out.println("ill play football!");
        }
    }
}
